class User {
  constructor(name, email, password) {
    this.name = name;
    this.email = email;
    this.password = password;
  }
}

class Post {
  constructor(id, author, image, text, likes) {
    this.id = id;
    this.author = author;
    this.image = image;
    this.text = text;
    this.likes = likes;
  }
}
