function charAt(string, index) {
    if (index < 0 || index >= string.length)
        return ''

    return string[index]
}

//El método charAt() es un método que se utiliza para obtener el carácter en una posición específica de una cadena. 
//Se usa llamando a la cadena a la que deseas acceder "string" y pasando como argumento el índice de la posición del carácter que deseas recuperar "index".


